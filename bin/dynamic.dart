void main() {
  //Tipe Data Dynamic
  //Dynamic bisa menampung tipe data apa saja
  //Bisa Menampung Number
  dynamic variabel = 100;
  print(variabel);

  //Bisa menampung Booolean
  variabel = true;
  print(variabel);

  //Bisa menampung String
  variabel = 'Hurray';
  print(variabel);

  //jika membuat var tanpa di deklarasikan langsung
  //mendjadi tipe data dynamic
  var contoh;
  contoh = 5;
  print(contoh);
}
